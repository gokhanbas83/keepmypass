class FaIcon extends StatelessWidget {
  final String iconName;
  final double size;
  final Color color;
  final String semanticLabel;
  final TextDirection textDirection;

  const FaIcon(
    this.iconName, {
    Key key,
    this.size,
    this.color,
    this.semanticLabel,
    this.textDirection,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return fa.FaIcon(
      FontAwesomeIconsMap.containsKey(iconName) ? FontAwesomeIconsMap[iconName] : fa.FontAwesomeIcons.ban,
      key: key,
      size: size,
      color: color,
      semanticLabel: semanticLabel,
      textDirection: textDirection,
    );
  }
}

