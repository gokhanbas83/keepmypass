String tr(String key) {
  return Translation.translate(key);
}

const Map<String, String> LanguageOptions = {
  'de': 'Deutsch',
  'en': 'English',
  'es': 'Español',
  'fr': 'Français',
  'pt': 'Português',
  'tr': 'Türkçe',
};

class Translation {
  String _lang;

  static Translation _instance;

  static void init(String lang) {
    _instance = Translation();
    _instance._lang = lang;
  }

  static bool isSupportedLanguage(String lang) {
    return LanguageOptions.containsKey(lang);
  }

  static String translate(String key) {
    if (TranslationData.containsKey(key) && TranslationData[key].containsKey(_instance._lang)) {
      return TranslationData[key][_instance._lang];
    }
    return key;
  }
}
